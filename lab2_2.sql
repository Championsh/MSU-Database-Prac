DROP SCHEMA IF EXISTS kvn CASCADE;
CREATE SCHEMA kvn;
SET search_path TO kvn;
DROP TYPE IF EXISTS role_name CASCADE;
CREATE TYPE role_name AS ENUM('Капитан', 'Ведущий', 'Актер', 
		 'Автор', 'Звукорежиссер', 'Администратор', 'Реквизитор', 'Чирлидер');


CREATE TABLE city
(
	city_id serial PRIMARY KEY,
	city_name text NOT NULL
);

CREATE TABLE kvn.university
(
	university_id serial PRIMARY KEY,
	university_name text NOT NULL,
	city_id integer REFERENCES kvn.city
		ON UPDATE CASCADE
);

CREATE TABLE kvn.team
(
	team_id serial PRIMARY KEY,
	team_name text NOT NULL,
	university_id integer REFERENCES kvn.university
		ON UPDATE CASCADE
);

CREATE TABLE kvn.personal
(
	player_id serial PRIMARY KEY,
	full_name text NOT NULL,
	phone_num varchar(14) NOT NULL,
	team_role role_name NOT NULL DEFAULT 'Актер'
);

CREATE TABLE kvn.player
(
	team_id integer REFERENCES kvn.team
		ON UPDATE CASCADE,
	player_id integer REFERENCES kvn.personal
		ON UPDATE CASCADE,
	begin_date timestamp NOT NULL,
	end_date timestamp CHECK(begin_date < end_date),
	UNIQUE(team_id, player_id)
);

CREATE TABLE kvn.league
(
	league_id serial PRIMARY KEY,
	league_name text NOT NULL
);

CREATE TABLE kvn.season
(
	league_id integer REFERENCES kvn.league
		ON UPDATE CASCADE,
	year_num integer CONSTRAINT season_year_check CHECK(1961 <= year_num 
				  AND year_num <= EXTRACT(year FROM current_date)),
	PRIMARY KEY(league_id, year_num)
);

CREATE TABLE kvn.state
(
	league_id integer,
	year_no integer,
	team_id integer REFERENCES kvn.team
		ON UPDATE CASCADE,
	place integer NOT NULL CHECK(place >= 1),
	FOREIGN KEY(league_id, year_no) REFERENCES kvn.season,
	UNIQUE(league_id, year_no, team_id)
);

CREATE TABLE kvn.location
(
	location_id serial PRIMARY KEY,
	city_id integer REFERENCES kvn.city
		ON UPDATE CASCADE,
	coords point NOT NULL,
	max_view bigint NOT NULL CHECK(max_view >= 0),
	if_live bool NOT NULL DEFAULT False
);

CREATE TABLE kvn.game
(
	game_id serial PRIMARY KEY,
    game_name text NOT NULL,
    league_id integer REFERENCES kvn.league
		ON UPDATE CASCADE,
    game_date timestamp NOT NULL,
	location_id integer REFERENCES kvn.location
		ON UPDATE CASCADE
);

CREATE TABLE kvn.result
(
	game_id integer REFERENCES kvn.game
		ON UPDATE CASCADE,
	result_id serial PRIMARY KEY,
	place integer NOT NULL CHECK(place >= 1),
	team_id integer REFERENCES kvn.team
		ON UPDATE CASCADE,
	if_pass bool NOT NULL DEFAULT False
);

CREATE TABLE kvn.score
(
	result_id integer REFERENCES kvn.result
		ON UPDATE CASCADE,
	round_num integer NOT NULL CHECK(round_num >= 1),
	score_amount numeric NOT NULL CHECK(score_amount >= 1.0),
	UNIQUE(result_id, round_num)
);

CREATE TABLE kvn.viewer
(
    game_id integer REFERENCES kvn.game
		ON DELETE CASCADE ON UPDATE CASCADE,
    online_view_amount bigint 
		NOT NULL CHECK(online_view_amount >= 0),
    offline_view_amount bigint
		NOT NULL CHECK(offline_view_amount >= 0)
);
